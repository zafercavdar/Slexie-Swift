import UIKit

extension UIColor {
    
    static func coreColor() -> UIColor {
        return UIColor.blackColor()
    }
    
    static func reddishColor() -> UIColor {
        return UIColor(red: 255.0 / 255.0, green: 59.0 / 255.0 , blue: 48.0 / 255.0, alpha: 1)
    }
}